#!/usr/bin/env node

import { existsSync } from "node:fs";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const CONTRACT_NAME = "PregradManager";
const NETWORK_CHAIN_IDS = {
  local: 31337,
  arcTestnet: 5_042_002,
};

const scriptDir = dirname(fileURLToPath(import.meta.url));
const protocolRoot = resolve(scriptDir, "..");
const artifactPath = resolve(
  protocolRoot,
  "artifacts/contracts/PregradManager.sol/PregradManager.json",
);
const deploymentsPath = resolve(protocolRoot, "deployments/protocol.json");
const outputPath = resolve(protocolRoot, "src/generated/pregrad-manager.ts");

const checkOnly = process.argv.includes("--check");

async function main() {
  const artifact = await readJson(artifactPath);
  assertArtifact(artifact);

  const deployments = normalizeDeployments(await readJson(deploymentsPath));
  const output = await formatTypeScript(
    renderMetadata({
      abi: artifact.abi,
      deployments,
    }),
  );

  if (checkOnly) {
    const current = existsSync(outputPath) ? await readFile(outputPath, "utf8") : "";

    if (current !== output) {
      console.error("Generated contract metadata is out of date. Run `pnpm --dir protocol build`.");
      process.exitCode = 1;
    }

    return;
  }

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, output);
}

async function readJson(path) {
  return JSON.parse(await readFile(path, "utf8"));
}

async function formatTypeScript(source) {
  const prettier = await import("prettier");
  const config = (await prettier.resolveConfig(outputPath)) ?? {};

  return prettier.format(source, {
    ...config,
    filepath: outputPath,
  });
}

function assertArtifact(artifact) {
  if (!artifact || artifact.contractName !== CONTRACT_NAME) {
    throw new Error(`Expected ${CONTRACT_NAME} artifact at ${artifactPath}`);
  }

  if (!Array.isArray(artifact.abi)) {
    throw new Error(`Expected ${CONTRACT_NAME} artifact to include an ABI`);
  }
}

function normalizeDeployments(rawDeployments) {
  if (!isPlainObject(rawDeployments)) {
    throw new Error("Expected protocol deployment registry to be an object");
  }

  const deployments = {};

  for (const [networkId, chainId] of Object.entries(NETWORK_CHAIN_IDS)) {
    const network = rawDeployments[networkId];

    if (!isPlainObject(network)) {
      throw new Error(`Missing deployment registry entry for ${networkId}`);
    }

    if (network.chainId !== chainId) {
      throw new Error(`Expected ${networkId} chainId ${chainId}, received ${network.chainId}`);
    }

    const contracts = network.contracts;
    if (!isPlainObject(contracts)) {
      throw new Error(`Expected ${networkId}.contracts to be an object`);
    }

    const deployment = contracts[CONTRACT_NAME];
    deployments[networkId] = {
      chainId,
      deployment: deployment === undefined ? undefined : normalizeDeployment(deployment),
    };
  }

  return deployments;
}

function normalizeDeployment(deployment) {
  if (!isPlainObject(deployment)) {
    throw new Error(`${CONTRACT_NAME} deployment must be an object`);
  }

  if (!isAddress(deployment.address)) {
    throw new Error(`${CONTRACT_NAME} deployment address is invalid`);
  }

  const normalized = {
    address: deployment.address,
  };

  if (deployment.deployBlock !== undefined) {
    normalized.deployBlock = normalizeDeployBlock(deployment.deployBlock);
  }

  return normalized;
}

function normalizeDeployBlock(value) {
  if (typeof value === "number") {
    if (!Number.isSafeInteger(value) || value < 0) {
      throw new Error(`${CONTRACT_NAME} deployBlock number must be a non-negative safe integer`);
    }

    return String(value);
  }

  if (typeof value !== "string" || !/^(0|[1-9]\d*)$/.test(value)) {
    throw new Error(`${CONTRACT_NAME} deployBlock must be a non-negative decimal string`);
  }

  return value;
}

function isPlainObject(value) {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function isAddress(value) {
  return typeof value === "string" && /^0x[a-fA-F0-9]{40}$/.test(value);
}

function renderMetadata({ abi, deployments }) {
  return `// This file is generated by scripts/export-contract-metadata.mjs.
// Do not edit it directly.

import type { Abi } from "viem";

export const protocolContractNames = ["${CONTRACT_NAME}"] as const;
export type ProtocolContractName = (typeof protocolContractNames)[number];

export const protocolNetworkIds = ${JSON.stringify(
    Object.keys(NETWORK_CHAIN_IDS),
    null,
    2,
  )} as const;
export type ProtocolNetworkId = (typeof protocolNetworkIds)[number];

export type ProtocolAddress = \`0x\${string}\`;

export type ProtocolContractDeployment = {
  readonly address: ProtocolAddress;
  readonly deployBlock?: bigint;
};

export type ProtocolNetworkDeployment = {
  readonly chainId: number;
  readonly contracts: Partial<
    Record<ProtocolContractName, ProtocolContractDeployment>
  >;
};

export type ProtocolDeployments = Record<
  ProtocolNetworkId,
  ProtocolNetworkDeployment
>;

export type PregradManagerDeploymentMap = Record<
  ProtocolNetworkId,
  ProtocolContractDeployment | undefined
>;

export const pregradManagerAbi = ${JSON.stringify(abi, null, 2)} as const satisfies Abi;

export const protocolDeployments = ${renderProtocolDeployments(
    deployments,
  )} as const satisfies ProtocolDeployments;

export const pregradManagerDeployments = ${renderPregradManagerDeployments(
    deployments,
  )} as const satisfies PregradManagerDeploymentMap;

export const pregradManagerContract = {
  name: "${CONTRACT_NAME}",
  abi: pregradManagerAbi,
  deployments: pregradManagerDeployments,
} as const;
`;
}

function renderProtocolDeployments(deployments) {
  const lines = ["{"];

  for (const [networkId, { chainId, deployment }] of Object.entries(deployments)) {
    lines.push(`  ${networkId}: {`);
    lines.push(`    chainId: ${chainId},`);
    lines.push("    contracts: {");

    if (deployment) {
      lines.push(`      ${CONTRACT_NAME}: ${renderDeployment(deployment)},`);
    }

    lines.push("    },");
    lines.push("  },");
  }

  lines.push("}");
  return lines.join("\n");
}

function renderPregradManagerDeployments(deployments) {
  const lines = ["{"];

  for (const [networkId, { deployment }] of Object.entries(deployments)) {
    lines.push(`  ${networkId}: ${deployment ? renderDeployment(deployment) : "undefined"},`);
  }

  lines.push("}");
  return lines.join("\n");
}

function renderDeployment({ address, deployBlock }) {
  const lines = ["{"];
  lines.push(`  address: "${address}",`);

  if (deployBlock !== undefined) {
    lines.push(`  deployBlock: ${deployBlock}n,`);
  }

  lines.push("}");
  return lines.join("\n");
}

await main();
